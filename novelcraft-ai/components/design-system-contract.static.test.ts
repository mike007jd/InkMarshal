  // INV-DD-03 — content-level empty states use the Empty family; loading and
  // error stay separately identifiable.
  it('keeps audited content surfaces on the Empty primitive', () => {
    const violations: string[] = [];

    for (const rel of EMPTY_STATE_SURFACES) {
      const src = readFileSync(join(process.cwd(), rel), 'utf8');
      if (!src.includes(EMPTY_PRIMITIVE)) violations.push(`${rel}: does not import ${EMPTY_PRIMITIVE}`);
      if (!src.includes('<Empty')) violations.push(`${rel}: does not render Empty`);
      const emptyTags = openingTags(src, 'Empty');
      if (!emptyTags.some(tag => hasClassTokens(tag, src, EMPTY_DENSITY_CONTRACTS[rel]))) {
        violations.push(`${rel}: Empty density contract drifted`);
      }
      if (hasAdHocEmptyWrapper(src)) violations.push(`${rel}: ad-hoc centered empty-state wrapper`);
    }

    // Loading and error are their own branches — an Empty must never be the
    // only thing these surfaces can render while busy or broken.
    const list = readFileSync(join(process.cwd(), 'components/conversations/ConversationList.tsx'), 'utf8');
    expect(list).toMatch(/loading && conversations\.length === 0/);
    expect(list).toMatch(/\{error && \(/);

    const thread = readFileSync(join(process.cwd(), 'components/conversations/ConversationThread.tsx'), 'utf8');
    expect(thread).toMatch(/loading \?[\s\S]{0,200}Spinner/);

    const usage = readFileSync(join(process.cwd(), 'components/studio/usage-panel.tsx'), 'utf8');
    expect(usage).toMatch(/\{error && \(/);
    expect(usage).toMatch(/!error && !loading && !hasData/);

    const knowledge = readFileSync(join(process.cwd(), 'components/knowledge/KnowledgePanel.tsx'), 'utf8');
    expect(knowledge).toMatch(/loading && !entries\.length/);

    expect(violations).toEqual([]);
  });

