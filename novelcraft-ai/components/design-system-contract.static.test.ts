function collectTsxFiles(dir: string): string[] {
  const out: string[] = [];
  for (const name of readdirSync(dir)) {
    const path = join(dir, name);
    const rel = relative(process.cwd(), path);
    if (rel.startsWith('components/ui/')) continue;
    const stat = statSync(path);
    if (stat.isDirectory()) {
      out.push(...collectTsxFiles(path));
    } else if (path.endsWith('.tsx') && !path.endsWith('.test.tsx')) {
      // Test files assert against rendered markup (e.g. locating a `<button`
      // tag in renderToStaticMarkup output), which is not a hand-rolled
      // control — the contract guards business UI surfaces only.
      out.push(path);
    }
  }
  return out;
}

