/**
 * Extracts JSX opening tags for `name`, tolerating arrows, nested braces,
 * strings and comments inside the tag (`onClick={() => …}`, `// it's fine`).
 */
function openingTags(src: string, name: string): string[] {
  const out: string[] = [];
  const re = new RegExp(`<${name}(?![A-Za-z0-9_])`, 'g');
  let match: RegExpExecArray | null;
  while ((match = re.exec(src))) {
    let i = match.index + match[0].length;
    let depth = 0;
    for (; i < src.length; i++) {
      const c = src[i];
      if (c === '"' || c === "'" || c === '`') {
        const quote = c;
        for (i++; i < src.length && src[i] !== quote; i++) if (src[i] === '\\') i++;
        continue;
      }
      if (c === '/' && src[i + 1] === '/') {
        while (i < src.length && src[i] !== '\n') i++;
        continue;
      }
      if (c === '/' && src[i + 1] === '*') {
        i = src.indexOf('*/', i + 2);
        if (i < 0) return out;
        i++;
        continue;
      }
      if (c === '{') depth++;
      else if (c === '}') depth--;
      else if (c === '>' && depth === 0) break;
    }
    out.push(src.slice(match.index, i + 1));
  }
  return out;
}

/** Opening tag + children, so a rule can assert on what a control renders. */
function elements(src: string, name: string): string[] {
  let cursor = 0;
  return openingTags(src, name).map(tag => {
    const start = src.indexOf(tag, cursor);
    if (start < 0) return tag;
    const close = src.indexOf(`</${name}>`, start + tag.length);
    cursor = close < 0 ? start + tag.length : close + `</${name}>`.length;
    return close < 0 ? tag : src.slice(start, close);
  });
}

/** Inlines `className={CONST}` / `${CONST}` so a class constant can't launder a radius. */
function classSources(tag: string, src: string): string {
  let resolved = tag;
  for (const [, ident] of tag.matchAll(/(?:className=\{|\$\{)([A-Za-z_$][\w$]*)[}\s]/g)) {
    const decl = src.match(new RegExp(`const\\s+${ident}\\s*(?::[^=]+)?=\\s*([\\s\\S]*?);`));
    resolved += decl ? decl[1] : ` __unresolved_class_source:${ident}`;
  }
  return resolved;
}

/** Order-independent signature for the pre-convergence centered empty wrapper. */
function hasAdHocEmptyWrapper(src: string): boolean {
  const required = ['flex', 'flex-col', 'items-center', 'justify-center', 'text-center'];
  return openingTags(src, 'div').some(tag => {
    const classes = classSources(tag, src);
    return required.every(token => new RegExp(`(?:^|\\s)${token}(?:\\s|$)`).test(classes));
  });
}

function hasClassTokens(tag: string, src: string, required: readonly string[]): boolean {
  const tokens = new Set(classSources(tag, src).match(/[-A-Za-z0-9:[\]_/\.]+/g) ?? []);
  return required.every(token => tokens.has(token));
}

